# Cálculo de uma média entre quatro valores utilizando de função, sem a entrada do usuário.

def media_aritmetica(primeiro_bimestre, segundo_bimestre, terceiro_bimestre, quarto_bimestre):
    resultado = (primeiro_bimestre + segundo_bimestre + terceiro_bimestre + quarto_bimestre)/4
    return resultado

print(media_aritmetica(6, 8, 10, 5))
